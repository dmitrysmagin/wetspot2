// voice - opl3 voice (0-17)
// vol - midi velocity value
void fm_set_volume(int voice, int vol)
{
	vol = fm_level[voice] * fm_vol_table[vol] / 128;

	fm_write(fm_offset[voice] + 0x43, (63 - vol) | fm_keyscale[voice]);

	if((fm_feedback[voice] & 1) == 1)
		fm_write(fm_offset[voice] + 0x40, (63 - vol) | fm_keyscale[voice]);
}

// 6 - bass, 7 - snare, 8 - tom, 9 - cymbal, 10 - hihat
// which map to voices 6, 7, 8, 8, 7
void fm_set_drum_volume(int drumvoice, int volume)
{
	if(drumvoice == 6) {
		fm_set_volume(6, volume);
		return;
	}

	volume = fm_drum_level[drumvoice-6] * fm_vol_table[volume] / 128;
	fm_write(fm_drum_offset[drumvoice-6] + 0x40, (63 - volume) | fm_drum_keyscale[drumvoice-6]);
}

void mid_SetChannelVolume(int channel, int volume)
{
	// midi has all drums on channel 9
	if(mid_Mode == MODE_MID && channel == 9) {
		for(int i = 6; i <= 10; i++) {
			fm_set_drum_volume(i, fm_drum_level[i-6] * volume / 128);
		}
		return;
	}

	// mdi has drums on 6,7,8,9,10
	if(mid_Mode == MODE_MDI && fm_perc_mode == 1 && channel >= 6) {
		fm_set_drum_volume(channel, fm_drum_level[channel-6] * volume / 128);

		return;
	}

	// cmf has drums on 11,12,13,14,15
	if(mid_Mode == MODE_CMF && fm_perc_mode == 1 && channel >= 11) {
		fm_set_drum_volume(channel, fm_drum_level[channel-6-5] * volume / 128);

		return;
	}

	// roll thru voices, find all channels, remember last velos and set volume
	for(int i = 0; i < 18; (fm_perc_mode == 1 ? (i == 5 ? i = 9 : i++) : i++)) {
		if(fm_voices[i].Flag == 1 && fm_voices[i].Channel == channel) {
			// channel volume * last note volume / 128
			fm_set_volume(i, fm_voices[i].Volume * volume / 128 );
		}
	}
}

void fm_set_drum_trig(int drumvoice, int flag)
{
	fm_drum_BD &= ~fm_drum_fl[drumvoice-6];
	if(flag != 0) fm_drum_BD |= fm_drum_fl[drumvoice-6];
	fm_write(0xbd, fm_drum_BD);
}

void fm_load_mid_patch(int voice, unsigned char *p)
{
	int r;

	fm_keyscale[voice] = *(p + 3) & 0xc0; // save upper 2 bits of vol
	fm_level[voice] = 63 - (*(p + 3) & 0x3f);
	fm_feedback[voice] = *(p + 10) | 0x30; // save feedback + algo + stereo

	r = fm_offset[voice];

	fm_write(r + 0x20, *(p + 0));
	fm_write(r + 0x23, *(p + 1));

	fm_write(r + 0x40, *(p + 2));
	fm_write(r + 0x43, *(p + 3));

	fm_write(r + 0x60, *(p + 4));
	fm_write(r + 0x63, *(p + 5));

	fm_write(r + 0x80, *(p + 6));
	fm_write(r + 0x83, *(p + 7));

	fm_write(r + 0xe0, *(p + 8));
	fm_write(r + 0xe3, *(p + 9));

	fm_write(fm_v_offset[voice] + 0xc0, *(p + 10) | 0x30); // stereo
}

// 6 - bass, 7 - snare, 8 - tom, 9 - cymbal, 10 - hihat
// which map to voices 6, 7, 8, 8, 7
void fm_load_drum_patch(int drumvoice, unsigned char *p)
{
	int r;

	// bass load as ordinary
	if(drumvoice == 6) {
		fm_load_mid_patch(6, p);
		return;
	}

	r = fm_drum_offset[drumvoice-6];

	fm_drum_keyscale[drumvoice-6] = *(p + 2) & 0xc0; // save upper 2 bits of vol
	fm_drum_level[drumvoice-6] = 63 - (*(p + 2) & 0x3f);

	fm_write(r + 0x20, *(p + 0));
	fm_write(r + 0x40, *(p + 2));
	fm_write(r + 0x60, *(p + 4));
	fm_write(r + 0x80, *(p + 6));
	fm_write(r + 0xe0, *(p + 8));

	//if(drumvoice < 9)
	fm_write(fm_v_offset[fm_drum_voice[drumvoice-6]] + 0xc0, (*(p + 10) & 0x3F) | 0x30); // stereo
}

// finds voice
int fm_find_voice(int channel, int note)
{
	// if mdi, channel == voice
	if(mid_Mode == MODE_MDI) return channel;

	for(int i = 0; i < 18; (fm_perc_mode == 1 ? (i == 5 ? i = 9 : i++) : i++)) {
		if(fm_voices[i].Flag == 1 && fm_voices[i].Channel == channel && fm_voices[i].Note == note) return i;
	}

	return -1;
}

// rolls thru fm_voices and returns voice which has Flag==0
int fm_get_free_voice(int channel, int note)
{
	// if mdi, channel == voice
	if(mid_Mode == MODE_MDI) return channel;

	for(int i = 0; i < 18; (fm_perc_mode == 1 ? (i == 5 ? i = 9 : i++) : i++))
		if(fm_voices[i].Flag == 0) {
			/*for(int j = i; j < 18; (j == 5 ? j = 9 : j++))
				if(fm_voices[j].Flag == 0) return j;*/
			return i;
		}

	for(int i = 0; i < 18; (fm_perc_mode == 1 ? (i == 5 ? i = 9 : i++) : i++))
		if(fm_voices[i].Flag == 1 && fm_voices[i].Channel == channel) {
			return i;
		}

	return -1;
}

void mid_NoteOff(unsigned char channel, unsigned char note, unsigned char volume)
{
	int voice;

	// if normal mid
	if(mid_Mode == MODE_MID && channel == 9) { // if normal midi
		int drumvoice;

		// if out of range
		if(note < 35 || note > 81) return;

		drumvoice = mid_drum_patches[(note-35)*16 + 11];
		// shut voice if it wasn't off
		fm_set_drum_trig(drumvoice, 0);

		return;
	}

	if(fm_perc_mode == 1) {
		// mdi - drum voices 6,7,8,9,10
		if(mid_Mode == MODE_MDI && channel >= 6) {
			fm_set_drum_trig(channel, 0);
			return;
		}

		// cmf - drum voices 11,12,13,14,15
		if(mid_Mode == MODE_CMF && channel >= 11) {
			fm_set_drum_trig(channel-5, 0);
			return;
		}
	}

	// find the exact note we need to shut up
	voice = fm_find_voice(channel, note);

	if(voice == -1) {
		FPRINTF(stdout, "NoteOff failed\n");
		return;
	}

	fm_voices[voice].Flag = 0;
	fm_voices[voice].fData.noteon = 0;

	fm_write(0xa0 + fm_v_offset[voice], fm_voices[voice].fData.lo);
	fm_write(0xb0 + fm_v_offset[voice], fm_voices[voice].fData.hi);

	//fprintf(stdout, "NOTEOFF: FM: Voice: %i, Note: %i, Volume: %i\n", channel, note, volume);
}

void fm_mid_drum_on(unsigned char channel, unsigned char note, unsigned char volume)
{
	int drumvoice;

	// if out of range
	if(note < 35 || note > 81) return;
	drumvoice = mid_drum_patches[(note-35)*16 + 11];

	// shut voice if it wasn't off
	fm_set_drum_trig(drumvoice, 0);
	fm_set_drum_volume(drumvoice, 0);
	fm_load_drum_patch(drumvoice, &mid_drum_patches[(note-35)*16]);

	if(drumvoice == 6 || drumvoice == 8 /*|| drumvoice == 9*/) { // if 6 or 8 WHY NO SNARE DRUM HERE??
		fm_voices[drumvoice].fData.freq = fm_note_freq[mid_drum_notes[note-35] % 12];
		fm_voices[drumvoice].fData.octave = mid_drum_notes[note-35] / 12;

		fm_voices[drumvoice].fData.noteon = 0;
		fm_write(0xa0 + drumvoice, fm_voices[drumvoice].fData.lo);
		fm_write(0xb0 + drumvoice, fm_voices[drumvoice].fData.hi);
	}

	// note on
	fm_set_drum_volume(drumvoice, MidiChannels[channel].Volume * volume / 128);
	fm_set_drum_trig(drumvoice, 1);
	return;
}

void fm_mdi_drum_on(unsigned char drumvoice, unsigned char note, unsigned char volume)
{
	// shut voice if it wasn't off
	fm_set_drum_trig(drumvoice, 0);

	if(drumvoice < 9) { // if 6, 7 or 8
		fm_voices[drumvoice].fData.freq = fm_note_freq[(drumvoice == 6 ? 0 : 7)];
		fm_voices[drumvoice].fData.octave = 2;
		fm_voices[drumvoice].fData.noteon = 0; // 0 obligatory
		fm_write(0xa0 + drumvoice, fm_voices[drumvoice].fData.lo);
		fm_write(0xb0 + drumvoice, fm_voices[drumvoice].fData.hi);
	}

	// note on
	fm_set_drum_volume(drumvoice, MidiChannels[drumvoice].Volume * volume / 128);
	fm_set_drum_trig(drumvoice, 1);
}

void fm_set_note(unsigned char voice, unsigned char note, int pitchbend, int pitchrange)
{
	// this table holds frequency differences between notes
	static int logtable[12] = {20, 21, 23, 24, 26, 27, 29, 30, 33, 34, 37, 40};
	int pitchvalue, absnote, newnote;

	// make pitchbend a signed value
	if(pitchbend > 0x2000) pitchbend -= 0x2000;
	else pitchbend = 0 - (0x2000 - pitchbend);

	// take pitch range into account
	pitchvalue = pitchbend * pitchrange / 0x2000;

	// calculate "absolute note" and +/- pitchbend value
	absnote = ((note - 12) << 7) + pitchvalue;

	// cut off too low and too high values
	if(absnote < 0) absnote = 0;
	if(absnote >= 0x2f80) absnote = 0x2f7f; // 2f80

	// calculate the new note
	newnote = (absnote % (12 << 7)) >> 7;

	// calculate frequency + pitchbend cents recalc'ed into logarithmic values
	fm_voices[voice].fData.freq = fm_note_freq[newnote] + (absnote & 0x7f) * logtable[newnote] / 128;
	fm_voices[voice].fData.octave = absnote / (12 << 7);

	fm_voices[voice].fData.noteon = 1;

	fm_write(0xa0 + fm_v_offset[voice], fm_voices[voice].fData.lo);
	fm_write(0xb0 + fm_v_offset[voice], fm_voices[voice].fData.hi);
}

void mid_SetChannelPitchWheel(int channel)
{
	// roll thru voices, find all channels, remember last velos and set volume
	for(int i = 0; i < 18; (fm_perc_mode == 1 ? (i == 5 ? i = 9 : i++) : i++)) {
		if(fm_voices[i].Flag == 1 && fm_voices[i].Channel == channel) {
			fm_set_note(i, fm_voices[i].Note,
				MidiChannels[channel].PitchWheel, MidiChannels[channel].PitchRange);
		}
	}
}

void mid_NoteOn(unsigned char channel, unsigned char note, unsigned char volume)
{
	int voice;

	// if normal midi, percussion is always on
	if(mid_Mode == MODE_MID && channel == 9) { fm_mid_drum_on(channel, note, volume); return; }

	if(fm_perc_mode == 1) {
		// mdi - drum voices 6,7,8,9,10
		if(mid_Mode == MODE_MDI && channel >= 6) {
			fm_mdi_drum_on(channel, note, volume);
			return;
		}

		// cmf - drum voices 11,12,13,14,15
		if(mid_Mode == MODE_CMF && channel >= 11) {
			fm_mdi_drum_on(channel-5, note, volume);
			return;
		}
	}

	if(volume == 0) { mid_NoteOff(channel, note, 0); return; }

	// MID and CMF are polyphonic, voice is dynamic
	// MDI - monophonic, voice == channel (except for drums)
	voice = fm_get_free_voice(channel, note);
	if(voice == -1) {
		FPRINTF(stdout, "fm_get_free_voice failed\n");
		return;
	}

	fm_voices[voice].fData.noteon = 0;
	fm_write(0xb0 + fm_v_offset[voice], fm_voices[voice].fData.hi);

	fm_voices[voice].Flag = 1;
	fm_voices[voice].Channel = channel;
	fm_voices[voice].Note = note;
	fm_voices[voice].Volume = volume;

	// MID and CMF load patches dynamically
	// MDI - only by command
	if(mid_Mode == MODE_MID) fm_load_mid_patch(voice, &mid_patches[MidiChannels[channel].Patch*16]);
	/*else if(mid_Mode == MODE_CMF) fm_load_cmf_patch(voice, ... ); */

	// channel volume * note volume / 128
	fm_set_volume(voice, MidiChannels[channel].Volume * volume / 128);

	// set note with regard to pitchwheel and pitch sensivity
	fm_set_note(voice, note, MidiChannels[channel].PitchWheel, MidiChannels[channel].PitchRange);
}

