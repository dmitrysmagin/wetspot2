unsigned char fm_drum_BD;
unsigned char fm_perc_mode; // 0 - melo, 1 - perc
// same order as drumvoices - 6
unsigned char fm_drum_fl[] = {16, 8, 4, 2, 1};
unsigned char fm_drum_voice[] = {6, 7, 8, 8, 7};
unsigned char fm_drum_offset[] = {0x10, 0x14, 0x12, 0x15, 0x11};

unsigned char fm_feedback[18]; // bit 1 - algo
unsigned char fm_keyscale[18]; // upper 2 bits of volume
unsigned char fm_level[18]; // instrument volume (63-reg43)

