void mid_MdiPatchLoad(unsigned char channel, unsigned char *p)
{
	static TIMBRE dest;
	MDI_TIMBRE *pSrc = (MDI_TIMBRE *)p;

	// convert from .BNK format
	for(int i = 0; i <= 1; i++) {
		dest.r20[i].FrequencyMult = pSrc->op[i].FrequencyMult;
		dest.r20[i].KeyboardScalingRate = pSrc->op[i].EnvelopeScaling;
		dest.r20[i].Sustain = pSrc->op[i].Sustain;
		dest.r20[i].FrequencyVibrato = pSrc->op[i].FrequencyVibrato;
		dest.r20[i].AmplitudeVibrato = pSrc->op[i].AmplitudeVibrato;

		dest.r40[i].OutputLevel = pSrc->op[i].OutputLevel;
		dest.r40[i].ScalingLevel = pSrc->op[i].KeyScaleLevel;

		dest.r60[i].DecayRate = pSrc->op[i].DecayRate;
		dest.r60[i].AttackRate = pSrc->op[i].AttackRate;

		dest.r80[i].ReleaseRate = pSrc->op[i].ReleaseRate;
		dest.r80[i].SustainLevel = pSrc->op[i].SustainLevel;

		dest.re0[i].WaveForm = pSrc->WaveForm[i];
	}

	dest.rc0.Algorithm = pSrc->op[0].FmType ^ 1; // Was it inverted?? WTF??
	dest.rc0.Feedback = pSrc->op[0].Feedback;
	dest.rc0.Stereo = 3;

	// in perc mode voices 6,7,8,9,10 are percussive, 9 maps to 8, 10 maps to 7
	if(fm_perc_mode == 1 && channel >= 6) {
		fm_load_drum_patch(channel, (unsigned char *)&dest);
		return;
	}

	fm_load_mid_patch(channel, (unsigned char *)&dest);
}

void mid_MdiSetPercMode(unsigned char m)
{
	fm_perc_mode = (m & 1);
	fm_drum_BD = 0xc0 | ((m & 1) << 5);
	fm_write(0xBD, fm_drum_BD);
}

// reset all voices
void mid_Reset()
{
	// proper reset for mid, mdi, cmf
	if(mid_Mode == MODE_MID) { // normal mid
		fm_perc_mode = 1;
		fm_drum_BD = 0xe0;
	} else { // adlib mdi (1) or cmf (2)
		fm_perc_mode = 0;
		fm_drum_BD = 0xc0;
	}

	fm_write(0xBD, fm_drum_BD);

	// all notes off
	for(int i = 0; i < 18; i++) {
		fm_voices[i].Flag = 0;
		fm_voices[i].fData.noteon = 0;
		fm_write(0xb0 + fm_v_offset[i], fm_voices[i].fData.hi);
		fm_set_volume(i, 0);
	}
}

